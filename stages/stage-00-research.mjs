import { createNexusCard } from '../lib/nexus-client.mjs';
import { withRetry } from '../lib/retry.mjs';

/**
 * Weekly research stage: fetch trends and generate 3–5 story concept cards in NEXUS.
 * Run via cron (Mon 08:00 GST / 04:00 UTC).
 */
export async function runStage0() {
  console.log('🔍 Stage 0: Weekly Research — generating story concepts');

  const concepts = await withRetry(
    () => generateStoryConcepts(),
    { maxRetries: 3, baseDelayMs: 10000, stage: 0 }
  );

  const cardIds = [];
  for (const concept of concepts) {
    const cardId = await createNexusCard({
      title: `Story Concept: ${concept.title}`,
      description: [
        `**Theme:** ${concept.theme}`,
        `**Characters:** ${concept.characters.join(', ')}`,
        `**Synopsis:** ${concept.synopsis}`,
        `**Target duration:** ${concept.targetDurationSeconds}s`,
        `**Target age:** ${concept.targetAge}`,
        `\n---\n_Auto-generated by Stage 0 research. Approve to start production._`,
      ].join('\n'),
      task_type: 'story_proposal',
      priority: 'medium',
      stream: 'youtube',
    });

    cardIds.push({ cardId, concept });
    console.log(`  Created NEXUS card: ${concept.title} (${cardId})`);
  }

  console.log(`✅ Stage 0 complete: ${cardIds.length} story concepts created`);
  return cardIds;
}

/**
 * Generate story concepts using Claude (if API key available) or hardcoded samples.
 */
async function generateStoryConcepts() {
  if (!process.env.ANTHROPIC_API_KEY) {
    console.warn('No ANTHROPIC_API_KEY — using sample story concepts');
    return getSampleConcepts();
  }

  try {
    const Anthropic = (await import('@anthropic-ai/sdk')).default;
    const client = new Anthropic({ apiKey: process.env.ANTHROPIC_API_KEY });

    const trendSummary = await fetchTrendSummary();

    const message = await client.messages.create({
      model: 'claude-haiku-4-5-20251001',
      max_tokens: 1024,
      messages: [{
        role: 'user',
        content: `You are a creative director for @tinytamiltales, a Tamil kids animated YouTube channel.

Generate 3-5 story concepts for Tamil animated kids stories (target age 3-7).

Current trends / keywords to consider: ${trendSummary}

Return a JSON array of concept objects with this schema:
[{
  "title": "English title",
  "theme": "friendship/nature/bravery/honesty/etc",
  "characters": ["Character1", "Character2"],
  "synopsis": "2-3 sentence story synopsis in English",
  "targetDurationSeconds": 300,
  "targetAge": "3-7"
}]

Return ONLY the JSON array. No markdown. No explanation.`,
      }],
    });

    const raw = message.content[0]?.text?.trim();
    // Strip markdown code block if present
    const text = raw.replace(/^```(?:json)?\s*/i, '').replace(/\s*```$/i, '').trim();
    return JSON.parse(text);
  } catch (err) {
    console.error('Claude concept generation failed:', err.message);
    return getSampleConcepts();
  }
}

